_SCHEDULE_ATTR = "compgen.static_schedule"


@dataclass(frozen=True)
class DynamicDeviceFunctionSpec:
    """Pure-compute body for one device function under dynamic scheduling.

    The body must NOT perform any ``event.notify`` or ``event.wait`` --
    that synchronisation is auto-generated by the emitter from the IR's
    out_edges / in_edges.

    Attributes:
        name:        device-function symbol matching ``CallDeviceOp.device_func``.
        body_source: indented Triton source for the body (without signature).
                     May reference any data pointer, any constexpr, and
                     ``task_id`` (the int task coordinate).
    """

    name: str
    body_source: str


@dataclass(frozen=True)
class DynamicMegakernelLoweringSpec:
    """Caller-supplied wiring for the dynamic-scheduler emitter."""

    data_pointers: tuple[str, ...] = ()
    constexpr_args: tuple[str, ...] = ()
    device_functions: tuple[DynamicDeviceFunctionSpec, ...] = ()
    early_push: bool = False
    num_warps: int = 4
    num_stages: int = 2


@dataclass(frozen=True)
class DynamicMegakernelLoweringResult:
    """Result of dynamic-mode lowering.

    Attributes:
        kernel_name:     name of the emitted ``@triton.jit`` function.
        kernel_source:   full Python source (with @triton.jit decorator).
        launch_config:   ``{"grid": int, "num_warps": int, ...}``.
        event_layout:    one entry per event tensor.
        device_function_table: ``{kind_int -> device_func_name}``.
        initial_queue:   the (task_id, kind) entries that must be present
                         in ``queue_pool`` when the kernel launches
                         (i.e. tasks with no in_edges).
        consumer_table:  ``{(event_name, event_index) -> [(consumer_task_id, kind), ...]}``
                         indexed by event coord so distinct event indices
                         fire independent consumers (required for
                         dynamic-scheduler correctness).
        total_tasks:     total task count -- the kernel exits once
                         ``queue_head`` reaches this many pops.
        diagnostics:     non-fatal warnings.
    """

    kernel_name: str
    kernel_source: str
    launch_config: dict[str, Any] = field(default_factory=dict)
    event_layout: list[dict[str, Any]] = field(default_factory=list)
    device_function_table: dict[int, str] = field(default_factory=dict)
    initial_queue: list[tuple[int, int]] = field(default_factory=list)
    consumer_table: dict[tuple[str, int], list[tuple[int, int]]] = field(default_factory=dict)
    total_tasks: int = 0
    diagnostics: list[str] = field(default_factory=list)


