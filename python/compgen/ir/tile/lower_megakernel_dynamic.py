"""Dynamic-scheduler megakernel emitter (Algorithm 2 of the ETC paper).

Sibling of :mod:`compgen.ir.tile.lower_megakernel` (static scheduler).
Consumes an annotated ``event.graph`` whose policy is ``dynamic`` and
emits a persistent Triton kernel that

    * Maintains a global ready queue in GPU global memory (head + tail
      atomic int32 counters and a ``(MAX_QUEUE, 2)`` slot pool).
    * Pre-seeds the queue with every task that has no ``in_edges``.
    * Each program (== one SM) loops:

          slot = atomic_add(queue_head, 1)
          if slot >= TOTAL_TASKS: break
          while slot >= atomic_load(queue_tail): pass
          (task_id, task_kind) = queue_pool[slot]
          dispatch into _run_<func>(...)
          for each out_edge of this task:
              old = atomic_add(E + ev, -decrement)
              if old == decrement:
                  # event just triggered -- push every consumer for this event
                  for each consumer of ev:
                      tail = atomic_add(queue_tail, 1)
                      queue_pool[tail] = (consumer_task_id, consumer_kind)

This is faithful to ETC §3.2 + Appendix A (push/pop) and the
*early-push* optimisation Appendix E (pushing a consumer the moment its
producer is dispatched is supported via ``early_push=True`` -- see
:class:`DynamicMegakernelLoweringSpec`).

Bodies passed to this emitter must be **pure compute** -- they do not
contain ``tl.atomic_add`` / ``while`` event-coordination logic; the
emitter inserts that automatically based on the IR's ``in_edges`` /
``out_edges``.  This is the second half of the paper's "scheduling
transformation rewrites the body" claim.
"""

from __future__ import annotations

import json
import textwrap
