"""Materialize a user-space extension workspace from a gap_action_queue entry.

For one selected gap (chosen by ``gap_id``), build a directory under
``.crg-artifacts/extensions/<gap_kind>/<extension_id>/`` containing:

- ``gap_record.json`` — frozen byte-for-byte copy of the queue entry
- ``extension_contract.json`` — what the agent must fill, what verify checks
- ``reference.py`` — ground-truth callable (read-only)
- ``extension.py`` — **the only file the agent fills** (initial stub)
- ``manifest.yaml`` — extension metadata
- ``tests/test_extension.py`` — autogenerated differential tests
- ``results/`` — empty; verify writes here later

Locked files are sha256'd at materialize time and re-checked at verify
time so the agent can't sneak edits into them.

This module **never invokes an LLM**. The deterministic fill is a
separate step (``agent_decomp_fill``) so the materialize/verify/register
loop can be exercised on its own.
"""

from __future__ import annotations

import hashlib
import json
import re
from dataclasses import dataclass, field
from datetime import UTC, datetime
from pathlib import Path
from typing import Any

import yaml

from compgen.graph_compilation.hashing import sha256_file

_SANITIZE_RE = re.compile(r"[^A-Za-z0-9_]")

# Files locked at materialize time. ``manifest.yaml`` is intentionally
# absent: the agent (or Claude Code) updates it during the
# fill → verify → register loop. ``extension_contract.json`` is also
# absent because it carries the locked-files audit and including it
# in its own list would be a self-reference.
_LOCKED_FILES = ("gap_record.json", "reference.py", "README.md")
# Locked test file name (canonical per the 04 spec).
_TESTS_FILES = ("tests/test_extension_correctness.py",)
# Number of frozen (input, expected) test cases generated at materialize
# time. Drives ``inputs/case_NN.pt`` + ``expected/case_NN.pt`` and the
# test loop in ``test_extension_correctness.py``. Kept small (8) so
# materialize stays fast even for 6-model matrices.
_FROZEN_CASE_COUNT = 8


# --------------------------------------------------------------------------- #
# Schemas
# --------------------------------------------------------------------------- #


@dataclass(frozen=True)
class ExtensionContract:
    schema_version: str
    extension_id: str
    gap_id: str
    gap_kind: str
    action: str
    fx_target: str
    target_id: str
    fillable_files: tuple[str, ...]
    locked_files: tuple[str, ...]
    locked_files_sha256: dict[str, str]
    verification: dict[str, Any]
    created_at_utc: str
    # 04 spec additions: contract is self-describing without re-reading
    # gap_record.json. Lets verify-extension and register-extension run
    # offline against just this file.
    semantic_name: str = ""
    slug: str = ""
    shape_signature: dict[str, Any] = field(default_factory=dict)
    dtype_signature: dict[str, Any] = field(default_factory=dict)
    required_evidence: tuple[str, ...] = ()
    allowed_actions: tuple[str, ...] = ()

    def to_dict(self) -> dict[str, Any]:
        return {
            "schema_version": self.schema_version,
            "extension_id": self.extension_id,
            "gap_id": self.gap_id,
            "gap_kind": self.gap_kind,
            "action": self.action,
            "fx_target": self.fx_target,
            "target_id": self.target_id,
            "semantic_name": self.semantic_name,
            "slug": self.slug,
            "fillable_files": list(self.fillable_files),
            "locked_files": list(self.locked_files),
            "locked_files_sha256": dict(self.locked_files_sha256),
            "verification": self.verification,
            "shape_signature": self.shape_signature,
            "dtype_signature": self.dtype_signature,
            "required_evidence": list(self.required_evidence),
            "allowed_actions": list(self.allowed_actions),
            "created_at_utc": self.created_at_utc,
        }


@dataclass
class MaterializeResult:
    extension_id: str          # canonical: <kind>__<slug>__<target>__<sha8>
    extension_dir: Path        # filesystem path; .name is the short path_stem
    path_stem: str             # short form (no kind prefix); on-disk dir name
    gap_record: dict[str, Any]
    contract: ExtensionContract
    artifacts: dict[str, Path] = field(default_factory=dict)


# --------------------------------------------------------------------------- #
# Extension ID + path generation
# --------------------------------------------------------------------------- #


def _sanitize(s: str) -> str:
    return _SANITIZE_RE.sub("_", s).strip("_")[:64] or "unnamed"


def _short_hash(payload: str) -> str:
    return hashlib.sha256(payload.encode("utf-8")).hexdigest()[:8]


def make_extension_id(gap: dict[str, Any], target_id: str) -> str:
    """Stable, human-readable extension ID.

    Format: ``<sanitized_fx_target>__<target_id>__<sha8>``. The sha8 is
    over the canonical-JSON of the gap record so two materializations
    of the same gap produce the same ID, and a different shape/dtype
    signature produces a different ID.
    """
    fx_target_sanitized = _sanitize(gap.get("fx_target", "unknown"))
    target_sanitized = _sanitize(target_id)
    canonical = json.dumps(
        {
            "gap_kind": gap.get("gap_kind"),
            "fx_target": gap.get("fx_target"),
            "shape_signature": gap.get("shape_signature", {}),
            "dtype_signature": gap.get("dtype_signature", {}),
            "target_id": target_id,
        },
        sort_keys=True,
        separators=(",", ":"),
    )
    return f"{fx_target_sanitized}__{target_sanitized}__{_short_hash(canonical)}"


def extension_dir_for(gap: dict[str, Any], target_id: str, extensions_root: Path) -> Path:
    return (
        Path(extensions_root)
        / gap["gap_kind"]
        / make_extension_id(gap, target_id)
    )


# --------------------------------------------------------------------------- #
# Materialize a workspace
# --------------------------------------------------------------------------- #


def materialize_extension(
    gap: dict[str, Any],
    *,
    target_id: str,
    extensions_root: Path,
    overwrite: bool = False,
) -> MaterializeResult:
    """Build the workspace for one gap. Caller picks which gap to materialize.

    The returned :class:`MaterializeResult` carries the generated paths
    so the next step (``agent_decomp_fill``) knows where to write
    ``extension.py``.
    """
    extensions_root = Path(extensions_root).resolve()
    extension_dir = extension_dir_for(gap, target_id, extensions_root)
    if extension_dir.exists() and not overwrite:
        # Idempotent: if a workspace already exists with the same ID,
        # the gap_record content matches by construction (the ID is
        # content-derived). Just return the existing layout.
        return _reload_existing(extension_dir, gap)

    if extension_dir.exists():
        import shutil
        shutil.rmtree(extension_dir)
    extension_dir.mkdir(parents=True)
    (extension_dir / "tests").mkdir()
    (extension_dir / "results").mkdir()
    (extension_dir / "inputs").mkdir()
    (extension_dir / "expected").mkdir()

    # The canonical extension_id is the FULL ``<kind>__<slug>__<target>__<sha8>``
    # form produced by ``gap_naming.extension_id``. The on-disk directory name
    # is the SHORT form (no kind prefix) because the parent dir already carries
    # the kind — that's a *path_stem*, not the canonical id. Every extension_id
    # field in materialized artifacts MUST use the canonical form so downstream
    # consumers (registry, gap_action_queue, verify report, manifest) all agree.
    from compgen.graph_compilation.gap_naming import extension_id as _canonical_extension_id

    extension_id = _canonical_extension_id(
        gap_kind=gap["gap_kind"],
        fx_target=gap["fx_target"],
        target_id=target_id,
        shape_signature=gap.get("shape_signature", {}),
        dtype_signature=gap.get("dtype_signature", {}),
    )
    path_stem = extension_dir.name  # short form, used only as a filesystem name

    # 1. gap_record.json — frozen copy.
    gap_record_path = extension_dir / "gap_record.json"
    gap_record_text = json.dumps(gap, indent=2, sort_keys=True)
    gap_record_path.write_text(gap_record_text + "\n", encoding="utf-8")

    # 2. reference.py
    reference_path = extension_dir / "reference.py"
    reference_path.write_text(_render_reference(gap), encoding="utf-8")

    # 3. manifest.yaml — EDITABLE. Agent updates ``status`` from ``draft``
    #    to ``verified`` during the fill → verify → register loop.
    manifest_path = extension_dir / "manifest.yaml"
    manifest_path.write_text(_render_manifest(gap, extension_id, target_id), encoding="utf-8")

    # 4. tests/test_extension_correctness.py — runnable harness that loads
    #    the frozen (inputs/expected) cases and runs the differential check.
    tests_path = extension_dir / "tests" / "test_extension_correctness.py"
    tests_path.write_text(_render_correctness_tests(gap), encoding="utf-8")

    # 5. extension.py — STUB. Agent fills.
    extension_path = extension_dir / "extension.py"
    extension_path.write_text(_render_extension_stub(gap), encoding="utf-8")

    # 5b. README.md — agent-facing instructions. LOCKED.
    readme_path = extension_dir / "README.md"
    readme_path.write_text(_render_readme(gap, extension_id, target_id), encoding="utf-8")

    # 5c. inputs/case_NN.pt + expected/case_NN.pt — frozen test data.
    #     Each pair is a (random-but-seeded inputs tuple, reference output)
    #     captured by *running* the reference at materialize time. Verify
    #     uses these in addition to fresh random trials.
    frozen_cases = _generate_frozen_cases(gap, extension_dir)

    # 6. extension_contract.json — sha256 of every locked file we just wrote.
    locked_keys = list(_LOCKED_FILES) + list(_TESTS_FILES)
    # Add every frozen-case file under inputs/ + expected/ to the locked set
    # so an agent who edits them gets caught.
    for case_path in sorted(extension_dir.rglob("inputs/*")):
        locked_keys.append(case_path.relative_to(extension_dir).as_posix())
    for case_path in sorted(extension_dir.rglob("expected/*")):
        locked_keys.append(case_path.relative_to(extension_dir).as_posix())
    locked = {rel: sha256_file(extension_dir / rel) for rel in locked_keys}
    contract = ExtensionContract(
        schema_version="extension_contract_v1",
        extension_id=extension_id,
        gap_id=gap["gap_id"],
        gap_kind=gap["gap_kind"],
        action=_pick_default_action(gap),
        fx_target=gap["fx_target"],
        target_id=target_id,
        fillable_files=("extension.py", "manifest.yaml"),
        locked_files=tuple(locked_keys),
        locked_files_sha256=locked,
        verification={
            "num_random_inputs": 100,
            "frozen_cases": frozen_cases,
            "rtol": 1e-5,
            "atol": 1e-5,
            "seed": 0,
        },
        # Domain-specific descriptive fields (from the gap record) so the
        # contract is self-contained for downstream verify/register tools.
        semantic_name=gap.get("semantic_name", gap["fx_target"]),
        slug=gap.get("slug", _sanitize(gap["fx_target"])),
        shape_signature=gap.get("shape_signature", {}),
        dtype_signature=gap.get("dtype_signature", {}),
        required_evidence=tuple(gap.get("required_evidence", [])),
        allowed_actions=tuple(gap.get("allowed_actions", [])),
        created_at_utc=_utcnow(),
    )
    contract_path = extension_dir / "extension_contract.json"
    contract_path.write_text(
        json.dumps(contract.to_dict(), indent=2, sort_keys=True) + "\n", encoding="utf-8"
    )

    artifacts = {
        "gap_record": gap_record_path,
        "extension_contract": contract_path,
        "reference": reference_path,
        "manifest": manifest_path,
        "tests": tests_path,
        "extension": extension_path,
    }
    return MaterializeResult(
        extension_id=extension_id,
        extension_dir=extension_dir,
        path_stem=path_stem,
        gap_record=gap,
        contract=contract,
        artifacts=artifacts,
    )


def _reload_existing(extension_dir: Path, gap: dict[str, Any]) -> MaterializeResult:
    contract_path = extension_dir / "extension_contract.json"
    contract_obj = json.loads(contract_path.read_text(encoding="utf-8"))
    contract = ExtensionContract(
        schema_version=contract_obj["schema_version"],
        extension_id=contract_obj["extension_id"],
        gap_id=contract_obj["gap_id"],
        gap_kind=contract_obj["gap_kind"],
        action=contract_obj["action"],
        fx_target=contract_obj["fx_target"],
        target_id=contract_obj["target_id"],
        fillable_files=tuple(contract_obj["fillable_files"]),
        locked_files=tuple(contract_obj["locked_files"]),
        locked_files_sha256=dict(contract_obj["locked_files_sha256"]),
        verification=contract_obj["verification"],
        created_at_utc=contract_obj["created_at_utc"],
    )
    artifacts = {
        "gap_record": extension_dir / "gap_record.json",
        "extension_contract": contract_path,
        "reference": extension_dir / "reference.py",
        "manifest": extension_dir / "manifest.yaml",
        "tests": extension_dir / "tests" / "test_extension_correctness.py",
        "extension": extension_dir / "extension.py",
    }
    return MaterializeResult(
        # Trust the contract's stored extension_id (canonical full form)
        # over the directory name (short path_stem).
        extension_id=contract_obj["extension_id"],
        extension_dir=extension_dir,
        path_stem=extension_dir.name,
        gap_record=gap,
        contract=contract,
        artifacts=artifacts,
    )


